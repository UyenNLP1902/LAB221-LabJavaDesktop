    private void getTable(List<Stock> list) {
        tblModelStock.setRowCount(0);
        for (Stock stock : list) {
            tblModelStock.addRow(stock.toVector());
        }
    }
    
    private void showTable() {
        List<Stock> list = mng.getAllStocks();
        Collections.sort(list);
        
        getTable(list);
    }

