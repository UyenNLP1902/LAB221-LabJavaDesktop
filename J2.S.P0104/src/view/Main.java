    private void btnPrevActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrevActionPerformed
        // TODO add your handling code here:
        int row = tblViewStock.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please choose a row");
        } else if (row == 0) {
            JOptionPane.showMessageDialog(this, "This is already the first row");
        } else {
            tblViewStock.setRowSelectionInterval(row - 1, row - 1);
        }
    }//GEN-LAST:event_btnPrevActionPerformed

    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
        int row = tblViewStock.getSelectedRow();
        int lastRow = tblModelStock.getRowCount() - 1;
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please choose a row");
        } else if (row == lastRow) {
            JOptionPane.showMessageDialog(this, "This is already the last row");
        } else {
            tblViewStock.setRowSelectionInterval(row + 1, row + 1);
        }
    }//GEN-LAST:event_btnNextActionPerformed

