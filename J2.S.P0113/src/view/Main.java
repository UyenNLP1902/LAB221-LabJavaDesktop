    private void tblViewNameMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblViewNameMouseClicked
        // TODO add your handling code here:
        txtBookCode.setEditable(false);
        int row = tblViewName.getSelectedRow();
        String code = (String) tblModelName.getValueAt(row, 0);
        Book book = mng.searchByPrimaryKey(code);

        txtBookCode.setText(code);
        txtBookName.setText(book.getName());
        txtAuthor.setText(book.getAuthor());
        txtPublisher.setText(book.getPublisher());
        cbbYear.setSelectedItem(Integer.toString(book.getYear()));
        if (book.isRent()) {
            chbForRent.setSelected(true);
        } else {
            chbForRent.setSelected(false);
        }
    }//GEN-LAST:event_tblViewNameMouseClicked

    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        String code = txtBookCode.getText();
        String name = txtBookName.getText();
        String author = txtAuthor.getText();
        String publisher = txtPublisher.getText();
        String yearString = (String) cbbYear.getItemAt(cbbYear.getSelectedIndex());
        int year = Integer.parseInt(yearString);

        boolean isRent = chbForRent.isSelected();

        String valid = "";
        if (code.isEmpty()) {
            valid += "Code cannot be blank.\n";
        }
        if (name.isEmpty()) {
            valid += "Name cannot be blank.\n";
        }
        if (author.isEmpty()) {
            valid += "Author cannot be blank.\n";
        }
        if (publisher.isEmpty()) {
            valid += "Publisher cannot be blank.";
        }

        if (valid.isEmpty()) {
            if (mng.create(code, name, author, publisher, year, isRent)) {
                JOptionPane.showMessageDialog(this, "Added successfully!");
                mng.save();
                showTable();
            } else {
                JOptionPane.showMessageDialog(this, "Add failed! (Code cannot be duplicated)");
            }
        } else {
            JOptionPane.showMessageDialog(this, valid);
        }
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String code = txtBookCode.getText();
        String name = txtBookName.getText();
        String author = txtAuthor.getText();
        String publisher = txtPublisher.getText();
        String yearString = (String) cbbYear.getItemAt(cbbYear.getSelectedIndex());
        int year = Integer.parseInt(yearString);

        boolean isRent = chbForRent.isSelected();

        String valid = "";
        if (code.isEmpty()) {
            valid += "Code cannot be blank.\n";
        }
        if (name.isEmpty()) {
            valid += "Name cannot be blank.\n";
        }
        if (author.isEmpty()) {
            valid += "Author cannot be blank.\n";
        }
        if (publisher.isEmpty()) {
            valid += "Publisher cannot be blank.";
        }

        if (valid.isEmpty()) {
            mng.update(code, name, author, publisher, year, isRent);
            JOptionPane.showMessageDialog(this, "Updated successfully!");
            mng.save();
            showTable();
        } else {
            JOptionPane.showMessageDialog(this, valid);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        String code = txtBookCode.getText();
        String valid = "";
        if (code.isEmpty()) {
            valid += "Code cannot be blank.\n";
        }
        if (valid.isEmpty()) {
            if (mng.delete(code)) {
                JOptionPane.showMessageDialog(this, "Deleted successfully!");
                mng.save();
                showTable();
                btnClearActionPerformed(evt);
            } else {
                JOptionPane.showMessageDialog(this, "Delete failed!");
            }
        } else {
            JOptionPane.showMessageDialog(this, valid);
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

