    private void getTable(List<Book> list) {
        tblModelName.setRowCount(0);
        for (Book book : list) {
            tblModelName.addRow(book.toVector());
        }
    }

    private void showTable() {
        List<Book> list = mng.getAllBook();
        Collections.sort(list);

        getTable(list);
    }

