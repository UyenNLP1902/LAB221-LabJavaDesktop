    private void btnHideMsgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHideMsgActionPerformed
        String text = txtSecretText.getText();
        if (text.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please fill in the secret message.");
            return;
        }

        try {
            // TODO add your handling code here:
            String msg = txtSecretText.getText();
            String imgPath = txtImgPath.getText();
            
            this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
            if (Encryption.encrypt(msg, imgPath)) {
                JOptionPane.showMessageDialog(this, "Encrypt successfully!");

                showStego();
            } else {
                JOptionPane.showMessageDialog(this, "Encrypt failed!");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } finally {
            this.setCursor(Cursor.getDefaultCursor());
        }

    }//GEN-LAST:event_btnHideMsgActionPerformed

