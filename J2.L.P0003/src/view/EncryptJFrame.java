    private BufferedImage readImageFile(String imgPath) throws Exception {
        BufferedImage theImage = null;
        File p = new File(imgPath);
        theImage = ImageIO.read(p);
        return theImage;
    }

    private ImageIcon ResizeImage(String ImagePath, JLabel label) {
        ImageIcon myImage = new ImageIcon(ImagePath);
        Image img = myImage.getImage();
        Image newImg = img.getScaledInstance(label.getWidth(), label.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImg);
        return image;
    }

    private void showStego() {
        String path = "stego.png";
        lblStego.setIcon(ResizeImage(path, lblStego));
    }

